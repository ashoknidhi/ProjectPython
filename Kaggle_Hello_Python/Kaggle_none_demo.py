# Program to demonstrate the return of none from a function
# Returning None in python is similar to returning null in other languages
def my_none_function():
    # print("This function does not return anything explicitly. It will return None by default.")
    # No return statement here, so it will return None
    print()
my_none_function()
print(f"The output of the function is: {my_none_function()}")
# The output will be None since the function does not return any value explicitly